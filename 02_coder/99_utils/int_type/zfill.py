# ----------------------------------------------------
# 4桁の0埋め  0を左寄せ fstring(数値のまま)
# ----------------------------------------------------
# n=5なら0005, n=5555なら5555
n = int(input())
print(f"{n:04}")

# ----------------------------------------------------
# 5桁の0埋め  0を左寄せ zfill(文字列にして)
# ----------------------------------------------------
# n=5なら00005, n=55555なら55555
n = input()
print( n.zfill(5))

# ----------------------------------------------------
# 3桁の0埋め 0を右寄せ ljust(文字列にして)
# ----------------------------------------------------
# n=5なら500, n=555なら555
n = input()
print(n.ljust(3, '0'))
